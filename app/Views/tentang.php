    <h1>Tentang</h1>

    <p><strong>Tracer Study</strong> atau yang sering disebut sebagai survey alumni atau survey “follow up” adalah studi mengenai lulusan lembaga penyelenggara pendidikan tinggi. Studi ini mampu menyediakan berbagai informasi yang bermanfaat bagi kepentingan evaluasi hasil pendidikan tinggi dan selanjutnya dapat digunakan untuk penyempurnaan dan penjaminan kualitas lembaga pendidikan tinggi yang bersangkutan.</p>

    <p><strong>Tracer Study</strong> juga bermanfaat dalam menyediakan informasi penting mengenai hubungan antara pendidikan tinggi dan dunia kerja profesional, menilai relevansi pendidikan tinggi, informasi bagi pemangku kepentingan (stakeholders), dan kelengkapan persyaratan bagi akreditasi pendidikan tinggi.</p>

    <p>Perguruan tinggi perlu melaksanakan Tracer Study karena membutuhkan umpan balik dari alumni dalam usahanya untuk perbaikan sistem dan pengelolaan pendidikan.</p>

    <p>Pada tahun 2016 Politeknik Negeri Bandung telah melakukan Tracer Study untuk mahasiswa yang lulus pada tahun 2014. Tracer Study ini dilakukan secara online melalui email dan SMS.</p>

    <h2>Tujuan Tracer Study</h2>
    <p>Tujuan utama Tracer Study antara lain:</p>
    <ul>
        <li>Mengetahui keberadaan alumni Polban setelah tamat kuliah</li>
        <li>Membangun database alumni</li>
        <li>Mengidentifikasi profil kompetensi alumni</li>
        <li>Mengetahui relevansi pelaksanaan kurikulum dengan kebutuhan pasar kerja</li>
        <li>Memenuhi kriteria akreditasi BAN-PT</li>
    </ul>

    <h2>Manfaat Tracer Study</h2>
    <p>Manfaat dari Tracer Study mencakup:</p>
    <ul>
        <li>Database alumni berdasarkan Prodi dan angkatan</li>
        <li>Masukan penting bagi pengembangan perguruan tinggi</li>
        <li>Alat evaluasi relevansi pendidikan dengan dunia kerja</li>
        <li>Masukan untuk perbaikan kinerja dosen dan staf administrasi</li>
        <li>Evaluasi untuk peningkatan kurikulum</li>
        <li>Bahan akreditasi internasional</li>
        <li>Penguatan jaringan alumni</li>
    </ul>
